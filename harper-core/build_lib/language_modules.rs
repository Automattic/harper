    code.push_str("        }\n");
    code.push_str("    }\n");
    code.push_str("}\n\n");

    // LanguageFamily impl
    code.push_str("impl LanguageFamily {\n");
    code.push_str("    pub fn dict_suffix(&self) -> &'static str {\n");
    code.push_str("        match self {\n");
    
