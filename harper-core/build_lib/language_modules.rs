    code.push_str("    ///\n");
    code.push_str(
        "    /// If multiple dialects are used equally often, they will all be enabled in the returned\n",
    );
    code.push_str("    /// `DialectFlags`. On the other hand, if there is a single dialect that is used the most, it\n");
